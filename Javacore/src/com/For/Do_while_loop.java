package com.For;


 class Do_while_loop



{
//	 exit level checking
	
	public static void main(String[] args) {
		
		
		int i=0;
		do {             
			i++;
			
			System.out.println(i);
			
		} while (i<10);
		
		
		int j=10;
		do {
			j--;
			System.out.println(j);
			
		} while (j>1);
		
	}

}
