package com.For;

public class For_loop {
	
	public static void main(String[] args) {
		
	
		for (int i = 0; i<=10;i++) {
			
			System.out.println(i);
		}
			
		System.out.println("===========");
		
		for (int j = 10 ;j >=0;j--) {
			
			System.out.println(j);
			
		}
			
			
				
			
			}
		
		
		
			
			
		}

	
	
