package com.For;

public class Forloops_try {

	public static void main(String[] args) {
		
		int[]a= {10,20,30,40};
		
		for (int i = 0; i < a.length; i++) {
			
			System.out.println(a[i]);
			
			
			
			
		}
		
		for (int i= a.length-1 ;i>=0; i--) {
			System.out.println(a[i]);
			
		}
		
	}
}
