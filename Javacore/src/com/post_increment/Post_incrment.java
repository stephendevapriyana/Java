package com.post_increment;

public class Post_incrment {
	
	public static void main(String[] args) {
		
		int a=100;
		System.out.println(++a); 
		
//		this name pre incremnts
		
		
		int b=100;
		System.out.println(b++);
		System.out.println(b);
		
//		this name post incremnet
	}

}
